#as funções .is vão retornar em bool(True, False)

variavel1 = input('Digite um dado:')
print(variavel1.isnumeric())#mostra se é numerico
print(variavel1.isalpha())#mostra se é alfabetico
print(variavel1.isalnum())#mostra se é alfanumerico
print(variavel1.isupper())#mostra se todu q foi digitado esta em maiusculo